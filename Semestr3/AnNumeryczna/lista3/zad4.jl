function c(k)
	x0 = BigFloat(0)
	for i = 3:k
		x0 = sqrt(0.5 * (1 + x0))
	end
	return x0
end

function s(k)
	if(k == 2)
		return BigFloat(1)
	end
	return sqrt(BigFloat(0.5) * (1 - c(k-1)))
end

p(k) = (BigFloat(2)^(k-1)) * s(k)

t = 200
setprecision(t)



for k = 4:100
	@printf "%d:  %0.4g\n" k abs(BigFloat(pi) - p(k))
end

